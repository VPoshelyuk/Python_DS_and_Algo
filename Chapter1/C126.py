def arithmetics(a, b, c):
    if a+b == c: print("Summation works")
    if b-c == a: print("Substraction works")
    if a*b == c: print("Multiplication works")


arithmetics(4, 5, 9)
print("------------------")
arithmetics(1, 5, 4)
print("------------------")
arithmetics(0, 0, 0)
print("------------------")
